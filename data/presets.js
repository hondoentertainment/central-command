function createTool({
  name,
  url,
  category,
  description,
  accent = "amber",
  pinned = false,
  pinRank = null,
  surfaces = [],
  iconKey = "auto",
  shortcutLabel = "",
  openMode = "new-tab",
}) {
  return {
    id: crypto.randomUUID(),
    name,
    url,
    category,
    description,
    accent,
    pinned,
    pinRank: pinned ? pinRank : null,
    surfaces,
    iconKey,
    shortcutLabel,
    openMode,
  };
}

