#!/usr/bin/env node
/**
 * Build firebase.config.generated.js from environment variables.
 * Set FIREBASE_API_KEY, FIREBASE_AUTH_DOMAIN, FIREBASE_PROJECT_ID,
 * FIREBASE_STORAGE_BUCKET, FIREBASE_MESSAGING_SENDER_ID, FIREBASE_APP_ID
 * in Vercel or GitHub Actions to enable Firebase at deploy time.
 * Exits 0 (no-op) if none are set.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const envVars = {
  apiKey: process.env.FIREBASE_API_KEY,
  authDomain: process.env.FIREBASE_AUTH_DOMAIN,
  projectId: process.env.FIREBASE_PROJECT_ID,
  storageBucket: process.env.FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.FIREBASE_APP_ID,
};

const hasAny = Object.values(envVars).some((v) => v && String(v).trim());
if (!hasAny) {
  process.exit(0);
}

const config = {
  apiKey: envVars.apiKey ?? "",
  authDomain: envVars.authDomain ?? "",
  projectId: envVars.projectId ?? "",
  storageBucket: envVars.storageBucket ?? "",
  messagingSenderId: envVars.messagingSenderId ?? "",
  appId: envVars.appId ?? "",
};

const outDir = path.join(__dirname, "..", "config");
const outPath = path.join(outDir, "firebase.config.generated.js");
const content = `/**
 * Auto-generated by scripts/build-firebase-config.js from env vars.
 * Do NOT edit manually.
 */

export default ${JSON.stringify(config, null, 2)};
`;

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outPath, content, "utf8");
